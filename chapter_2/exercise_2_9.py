first_number = int(input("첫 번째 정수를 입력하세요.\n"))
second_number = int(input("두 번째 정수를 입력하세요.\n"))

print(f"{first_number} + {second_number} = {first_number + second_number}")
print(f"{first_number} - {second_number} = {first_number - second_number}")
print(f"{first_number} * {second_number} = {first_number * second_number}")
print(f"{first_number} / {second_number} = {first_number / second_number}")